-- 13. CUMULATIVE REVENUE: CALCULATE THE RUNNING TOTAL OF REVENUE DAY-BY-DAY FOR THE YEAR 2017
WITH Running_total AS
(SELECT SUM(price) AS daily_revenue, SUBSTRING(`order_purchase_timestamp`, 9,2) AS `Day`
FROM order_items i
JOIN orders o ON i.order_id = o.order_id
WHERE SUBSTRING(`order_purchase_timestamp`, 9,2) IS NOT NULL
GROUP BY `Day`)
-- NOW TO DO THE RUNNING SUN
SELECT `Day`, daily_revenue, SUM(daily_revenue) OVER(ORDER BY `Day`) AS running_t
FROM Running_total

-- cleaner method for num ber 13

WITH DailyRevenue AS (
SELECT
-- This creates the "Monday 01 January" format
DATE_FORMAT(o.order_purchase_timestamp, '%W %d %M') AS formatted_date,
-- We keep the raw date so SQL knows how to put them in order
DATE(o.order_purchase_timestamp) AS raw_date,
SUM(oi.price) AS daily_revenue
FROM orders o
JOIN order_items oi ON o.order_id = oi.order_id
-- Filter for the year 2017
WHERE o.order_purchase_timestamp >= '2017-01-01'
AND o.order_purchase_timestamp <= '2017-12-31'
GROUP BY raw_date, formatted_date
)
SELECT
formatted_date,
daily_revenue,
SUM(daily_revenue) OVER(ORDER BY raw_date) AS running_total
FROM DailyRevenue
ORDER BY raw_date;

-- 14. ORDER ITEM CONCENTRATION: FIND THE PCT CONTRBUTION OF EACH INDIVIDUAL ORDER ITEM TO ITS TOTAL ORDER VALUE
SELECT order_id, price, SUM(price + freight_value) OVER(PARTITION BY order_id) AS total_order_value,
(price/SUM(price + freight_value) OVER(PARTITION BY order_id)) * 100 item_pct
FROM order_items;

-- 15. RETENTION LAG: (WINDOWS FUNCTION) FOR REPEAT CUSTOMERS CALCULATE THE NUMBER OF DAYS BETWEEN THEIR FIRST
-- AND SECOND ORDER lag is oly fumction for looking bk to compare rows in the same column
WITH order_seq AS
(SELECT customer_unique_id, order_purchase_timestamp, LAG(order_purchase_timestamp)
OVER(PARTITION BY customer_unique_id ORDER BY order_purchase_timestamp) AS prev_order
FROM customers c
JOIN orders o ON c.customer_id = o.customer_id)

SELECT customer_unique_id, DATEDIFF(order_purchase_timestamp, prev_order) AS days_btw
FROM order_seq
WHERE prev_order IS NOT NULL;

-- 16. HIGH VALUE STATES CTE: FIND STATES WHERE AVERAGE ORDER VALUE IS HIGHER THAN OVERALL 
-- AVERAGE ORDER VALUE OF ENTIRE COMPANY
WITH StateAOV AS
(SELECT customer_state, AVG(price) AS avg_order_value_state
FROM customers c 
JOIN orders o ON c.customer_id = o.customer_id
JOIN order_items i ON o.order_id = i.order_id
GROUP BY customer_state)

SELECT *
FROM StateAOV
