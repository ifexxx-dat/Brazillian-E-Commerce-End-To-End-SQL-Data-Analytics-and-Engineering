SELECT order_id, COUNT(*) AS ordercount , (MONTHNAME (`order_purchase_timestamp`)) AS `MONTH`
FROM orders
GROUP BY order_id, `MONTH`;


-- 3. POPULAR CATEGORIES: WHICH PRODUCT CATEGORIES HAVE THE HIGHEST NUMBER OF ORDERS
SELECT product_id, COUNT(*) AS pdt_count
FROM order_items
GROUP BY product_id
ORDER BY pdt_count DESC
LIMIT 5;

-- 4. CUSTOMER LOCATION : WHICH 10 CITIES HAVE THE MOST CUSTOMER
SELECT  customer_city, COUNT(customer_unique_id) AS each_customer
FROM customers
GROUP BY customer_city
ORDER BY each_customer DESC
LIMIT 10;

-- 5. AVERAGE ORDER VALUE: ON AVERAGE HOW MUCH DOES A CUSTOMER SPEND PER TRANSACTIOB
