-- 7. SHIPPING IMPACT BY STATE: WHICH STATES ARE MOST EXPENSIVE TO SHIP TO
SELECT customer_state, AVG(freight_value) AS avg_shipping_cost
FROM customers c
JOIN orders o ON c.customer_id = o.customer_id
JOIN order_items i ON o.order_id = i.order_id
GROUP BY customer_state
ORDER BY avg_shipping_cost DESC;

-- 8. DELIVERY PERFORMANCE: WHAT IS THE AVERAGE DIFFERENCE BEYWEEN THE ESTIMATED DELIVERY DATE
-- AND ACTUAL DELIVERY DATE DATEDIFF() CALCULATES DIFFERENCE BETWEEN 2 DATES
SELECT order_estimated_delivery_date, order_delivered_customer_date, 
AVG(DATEDIFF(order_estimated_delivery_date, order_delivered_customer_date)) AS Delivery_performance
FROM ORDERS
GROUP BY order_estimated_delivery_date, order_delivered_customer_date;

-- 9. PEAK SHOPPINH HOUR: WHAT HOUR OF THE DAY IS RUSH HOUR
