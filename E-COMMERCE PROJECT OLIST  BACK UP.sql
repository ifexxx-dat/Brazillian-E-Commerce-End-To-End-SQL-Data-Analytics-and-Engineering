-- 11. CUSTOMER PURCHASE BEHAVIOUR: IDENTIFY THE FIRST AND LAST PURCHASE FOR EVERY UNIQUE CUSTOMER
WITH customer_purchase_behaviour AS
(SELECT customer_unique_id, order_purchase_timestamp
FROM customers c
JOIN orders o ON c.customer_id = o.customer_id)

SELECT customer_unique_id, MIN(order_purchase_timestamp), MAX(order_purchase_timestamp)
FROM customer_purchase_behaviour
GROUP BY customer_unique_id;

-- 12. TOP SPENDERS PER STATE: RANK THE TOP 3 CUSTOMERS IN EACH STATE BASED ON THEIR TOTAL SPEND
