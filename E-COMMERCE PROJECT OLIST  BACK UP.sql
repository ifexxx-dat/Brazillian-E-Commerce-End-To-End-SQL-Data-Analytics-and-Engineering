-- 17. SHIPPING EFFICIENCY: IDENTIFY ORDERS THAT TOOK LONGER THAN THE AVERAGE SHIPPING TIME FOR 
-- THEIR SPECIFIC DELIVERY STATE

SELECT
order_id,
customer_state,
actual_shipping_time,
avg_state_shipping
FROM (
SELECT
o.order_id,
c.customer_state,
DATEDIFF(o.order_delivered_customer_date, o.order_delivered_carrier_date) AS actual_shipping_time,
AVG(DATEDIFF(o.order_delivered_customer_date, o.order_delivered_carrier_date)) OVER(PARTITION BY c.customer_state) AS avg_state_shipping
FROM orders o
JOIN customers c ON o.customer_id = c.customer_id
WHERE o.order_delivered_customer_date IS NOT NULL
) AS shipping_stats
WHERE actual_shipping_time > avg_state_shipping;


-- 18.  MONTHLY SELLER PERFORMANCE: CALCULATE THE MONTH OVER MONTH GROWTH IN REVENUE FOR THE TOP PERFORMING SELLER
WITH SellerMonthly AS (
SELECT
seller_id,
DATE_FORMAT(o.order_purchase_timestamp, '%Y-%m') AS month,
SUM(oi.price) AS revenue
FROM orders o
JOIN order_items oi ON o.order_id = oi.order_id
WHERE seller_id = 'seller_id_here' -- Replace with a top seller ID
GROUP BY seller_id, month
)
SELECT
month,
revenue,
LAG(revenue) OVER(ORDER BY month) AS prev_month_revenue,
(revenue - LAG(revenue) OVER(ORDER BY month)) / LAG(revenue) OVER(ORDER BY month) * 100 AS growth_pct
FROM SellerMonthly;
